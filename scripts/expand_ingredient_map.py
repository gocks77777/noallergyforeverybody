def detect_allergens(ingredients: list[str]) -> list[str]:
    found = set()
    text = " ".join(ingredients).lower()
    for allergen, keywords in ALLERGEN_KEYWORDS.items():
        if any(kw in text for kw in keywords):
            found.add(allergen)
    return sorted(found)

